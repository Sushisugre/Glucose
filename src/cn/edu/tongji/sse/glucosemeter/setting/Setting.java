package cn.edu.tongji.sse.glucosemeter.setting;

public class Setting {
	
	private String unit;
	
	private int year_1st;
	private int year_2st;
	
	private int month_1st;
	private int month_2st;
	
	private int day_1st;
	private int day_2st;
	
	private int hour_1st;
	private int hour_2st;
	
	private int minute_1st;
	private int minute_2st;
	
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public int getYear_1st() {
		return year_1st;
	}
	public void setYear_1st(int year_1st) {
		this.year_1st = year_1st;
	}
	public int getYear_2st() {
		return year_2st;
	}
	public void setYear_2st(int year_2st) {
		this.year_2st = year_2st;
	}
	public int getMonth_1st() {
		return month_1st;
	}
	public void setMonth_1st(int month_1st) {
		this.month_1st = month_1st;
	}
	public int getMonth_2st() {
		return month_2st;
	}
	public void setMonth_2st(int month_2st) {
		this.month_2st = month_2st;
	}
	public int getDay_1st() {
		return day_1st;
	}
	public void setDay_1st(int day_1st) {
		this.day_1st = day_1st;
	}
	public int getDay_2st() {
		return day_2st;
	}
	public void setDay_2st(int day_2st) {
		this.day_2st = day_2st;
	}
	public int getHour_1st() {
		return hour_1st;
	}
	public void setHour_1st(int hour_1st) {
		this.hour_1st = hour_1st;
	}
	public int getHour_2st() {
		return hour_2st;
	}
	public void setHour_2st(int hour_2st) {
		this.hour_2st = hour_2st;
	}
	public int getMinute_1st() {
		return minute_1st;
	}
	public void setMinute_1st(int minute_1st) {
		this.minute_1st = minute_1st;
	}
	public int getMinute_2st() {
		return minute_2st;
	}
	public void setMinute_2st(int minute_2st) {
		this.minute_2st = minute_2st;
	}
	
}
